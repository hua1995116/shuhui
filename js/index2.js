
$(function(){
	$('.case1 .index_li1').hover(function(){
		$('.mask1').animate({'opacity':0.3},300);
	},function(){
		if(!$('.mask1').is(':animated')){
			$('.mask1').animate({'opacity':0},300);
		}
	})
})
